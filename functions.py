def area(width, height):
    result = width * height
    return result


result = area(5, 6)
print(result)

result2 = area(9, 21)
print(result2)

result3 = area(21, 21)
print(result3)


def subtract(num1, num2):
    result = num1 - num2
    return result


diff = subtract(420, 69)
print(diff)
diff2 = subtract(0, 99)
print(diff2)


